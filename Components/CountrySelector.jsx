const CountrySelector = ({ selectedCountry, onSelectCountry }) => {
  const [modalVisible, setModalVisible] = React.useState(false);
  
  const selectedCountryName = countries.find(c => c.code === selectedCountry)?.name || 'Select Country';

  return (
    <View style={styles.container}>
      <TouchableOpacity 
        style={styles.selectorButton} 
        onPress={() => setModalVisible(true)}
      >
        <Text style={styles.selectorText}>{selectedCountryName}</Text>
        <Ionicons name="chevron-down" size={18} color="#fff" />
      </TouchableOpacity>

      <Modal
        animationType="slide"
        transparent={true}
        visible={modalVisible}
        onRequestClose={() => setModalVisible(false)}
      >
        <View style={styles.modalContainer}>
          <View style={styles.modalContent}>
            <View style={styles.modalHeader}>
              <Text style={styles.modalTitle}>Select Country</Text>
              <TouchableOpacity onPress={() => setModalVisible(false)}>
                <Ionicons name="close" size={24} color="#333" />
              </TouchableOpacity>
            </View>
            
            <FlatList
              data={countries}
              keyExtractor={(item) => item.code}
              renderItem={({ item }) => (
                <TouchableOpacity
                  style={[
                    styles.countryItem,
                    selectedCountry === item.code && styles.selectedCountryItem
                  ]}
                  onPress={() => {
                    onSelectCountry(item.code);
                    setModalVisible(false);
                  }}
                >
                  <Text style={[
                    styles.countryText,
                    selectedCountry === item.code && styles.selectedCountryText
                  ]}>
                    {item.name} ({item.code.toUpperCase()})
                  </Text>
                  {selectedCountry === item.code && (
                    <Ionicons name="checkmark" size={20} color="#1e88e5" />
                  )}
                </TouchableOpacity>
              )}
            />
          </View>
        </View>
      </Modal>
    </View>
  );
};

